package org.lareferencia.core.entity.services.exception;

import org.lareferencia.core.entity.domain.EntityRelationException;

import lombok.Getter;
import lombok.Setter;

public class EntitiyRelationXMLLoadingException extends EntityRelationException {

   	private static final long serialVersionUID = -4313080671067089014L;

    @Getter @Setter
    String fileName = "unknown";

    public EntitiyRelationXMLLoadingException(String message, String fileName) {
        super(message);
        this.fileName = fileName;
    }

    public EntitiyRelationXMLLoadingException(String message) {
        super(message);
    }

    public EntitiyRelationXMLLoadingException(String message, Throwable cause) {
        super(message, cause);
    }

    public EntitiyRelationXMLLoadingException(Throwable cause) {
        super(cause);
    }

    public EntitiyRelationXMLLoadingException(String message, Throwable cause, boolean enableSuppression,
            boolean writableStackTrace) {
        super(message, cause, enableSuppression, writableStackTrace);

    }

    

}
